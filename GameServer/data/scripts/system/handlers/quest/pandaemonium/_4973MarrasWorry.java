package quest.pandaemonium;

import gameserver.model.gameobjects.player.Player;
import gameserver.questEngine.handlers.QuestHandler;
import gameserver.questEngine.model.QuestCookie;
import gameserver.questEngine.model.QuestState;
import gameserver.questEngine.model.QuestStatus;


/**
 * @author XRONOS
 *
 */

public class _4973MarrasWorry extends QuestHandler
{
	private final static int	questId	= 4973;
	private final static int[]	npcs = {798392, 204130, 204728, 204324};
	
	public _4973MarrasWorry()
	{
		super(questId);
	}

	@Override
	public void register()
	{
		qe.setNpcQuestData(798392).addOnQuestStart(questId);
		for(int npc: npcs)
			qe.setNpcQuestData(npc).addOnTalkEvent(questId);
	}

	@Override
	public boolean onDialogEvent(QuestCookie env)
	{
		Player player = env.getPlayer();
		QuestState qs = player.getQuestStateList().getQuestState(questId);
		
		if(defaultQuestNoneDialog(env, 798392))
			return true;

		if(qs == null)
			return false;

		int var = qs.getQuestVarById(0);
		
		if(qs.getStatus() == QuestStatus.START)
		{
			switch (env.getTargetId())
			{
				case 204130:
					switch(env.getDialogId())
					{
						case 25:
							if(var == 0)
								return sendQuestDialog(env, 1352);
						case 10000:
							return defaultCloseDialog(env, 0, 1);
					}
					break;
				case 204728:
					switch(env.getDialogId())
					{
						case 25:
							if(var == 1)
								return sendQuestDialog(env, 1693);
						case 10001:
							return defaultCloseDialog(env, 1, 2);
					}
					break;
				case 204324:
					switch(env.getDialogId())
					{
						case 25:
							if(var == 2)
								return sendQuestDialog(env, 2034);
						case 10002:
							return defaultCloseDialog(env, 2, 3, true, false);
					}
					break;
			}
		}
		return defaultQuestRewardDialog(env, 798392, 2375);
	}
}
